/** S15 样条段数 */
const S15_STRIDE = 6;

/**
 * S15 三次样条求值
 */
function evalS15(y: number): number {
  const d = S15_SPLINE;
  const n = d.length / S15_STRIDE;
  for (let i = 0; i < n; i++) {
    const off = i * S15_STRIDE;
    const x0 = d[off]!,
      x1 = d[off + 1]!;
    if (y >= x0 && y < x1) {
      const t = (y - x0) / (x1 - x0);
      const a0 = d[off + 5]!,
        a1 = d[off + 4]!,
        a2 = d[off + 3]!,
        a3 = d[off + 2]!;
      return a0 + a1 * t + a2 * t * t + a3 * t * t * t;
    }
  }
  // y 刚好等于最后一段的 x1
  const last = (n - 1) * S15_STRIDE;
  const a0 = d[last + 5]!,
    a1 = d[last + 4]!,
    a2 = d[last + 3]!,
    a3 = d[last + 2]!;
  return a0 + a1 + a2 + a3;
}

/**
 * 非均匀 Catmull-Rom 样条插值 (用于 ANNUAL_DATA 段)
 */
function catmullRom(
  t0: number,
  t1: number,
  t2: number,
  t3: number,
  p0: number,
  p1: number,
  p2: number,
  p3: number,
  t: number,
): number {
  const dt1 = t2 - t1;
  const x = (t - t1) / dt1;
  const m1 = ((p2 - p0) / (t2 - t0)) * dt1;
  const m2 = ((p3 - p1) / (t3 - t1)) * dt1;
  const x2 = x * x;
  const x3 = x2 * x;
  return (
    (2 * x3 - 3 * x2 + 1) * p1 +
    (x3 - 2 * x2 + x) * m1 +
    (-2 * x3 + 3 * x2) * p2 +
    (x3 - x2) * m2
  );
}

/**
 * 二次外推（用于数据表范围之外的年份）
 */
function dtExtrapolate(y: number): number {
  const u = (y - 1820) / 100;
  return -20 + 32 * u * u;
}

/** S15 样条覆盖的范围 */
const S15_START = -720;
const S15_END = 1953;

/**
 * 计算 ΔT = TT - UT1（单位：秒）
 *
 * @param y 公历年份（可含小数，如 2024.5 表示年中）
 */
export function deltaT(y: number): number {
  // 1) S15 样条范围
  if (y >= S15_START && y < S15_END) {
    return evalS15(y);
  }

  // 2) 每年数据范围
  const data = ANNUAL_DATA;
  const firstYear = data[0]![0];
  const lastYear = data[data.length - 1]![0];

  if (y >= firstYear && y < lastYear) {
    let i = 0;
    for (; i < data.length - 1; i++) {
      if (y < data[i + 1]![0]) break;
    }
    const i0 = Math.max(i - 1, 0);
    const i1 = i;
    const i2 = Math.min(i + 1, data.length - 1);
    const i3 = Math.min(i + 2, data.length - 1);
    return catmullRom(
      data[i0]![0],
      data[i1]![0],
      data[i2]![0],
      data[i3]![0],
      data[i0]![1],
      data[i1]![1],
      data[i2]![1],
      data[i3]![1],
      y,
    );
  }

  // 3) S15 之前：二次外推
  if (y < S15_START) return dtExtrapolate(y);

  // 4) 数据表之后：平滑过渡到外推公式
  const t0 = lastYear;
  const v0 = data[data.length - 1]![1];
  if (y > t0 + 100) return dtExtrapolate(y);
  const v = dtExtrapolate(y);
  const dv = dtExtrapolate(t0) - v0;
  return v - (dv * (t0 + 100 - y)) / 100;
}

/** 每天的秒数 */
const SECONDS_PER_DAY = 86400;

/** 每儒略年的天数 */
const DAYS_PER_JULIAN_YEAR = 365.25;

/**
 * 计算 ΔT = TT - UT1（单位：天）
 *
 * @param ut J2000.0 起算的儒略日数
 */
export function deltaTDays(ut: number): number {
  const y = 2000 + ut / DAYS_PER_JULIAN_YEAR;
  return deltaT(y) / SECONDS_PER_DAY;
}

/**
 * 将 UT 转换为 TT
 *
 * @param ut J2000.0 起算的儒略日数
 */
export function terrestrialTime(ut: number): number {
  return ut + deltaTDays(ut);
}
